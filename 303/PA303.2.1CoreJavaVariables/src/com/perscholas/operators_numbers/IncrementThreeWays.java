package com.perscholas.operators_numbers;

public class IncrementThreeWays {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        x += 1;
        System.out.println(x);
        x++;
        System.out.println(x);
        ++x;
        System.out.println(x);
        x = x + 1;
        System.out.println(x);
    }
}
